package cn.lanqiao.start;

import java.util.Scanner;

/**
 * 矩形拼接
 */
public class Simple05 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        for (int i = 0; i < t; i++) {
            int[] nums = new int[6];
            for (int j = 0; j < 6; j++) {
                nums[j] = sc.nextInt();
            }

        }
        sc.close();
    }
}
